                                            </a>
                                            <div class="collapse" id="Q5">
                                                <div class="collapsible-contents-panel faq-answer">
                                                    You need to login into the SMC-Citizen Portal with your registered email or mobile number and password. You can either click <span class="primary-color">"Recent Transactions"</span> link at the top right corner or by selecting the desired service and then clicking <span class="primary-color">"Recent Transactions"</span> link on the right hand side of the page. On clicking this link, you will be shown the recent transactions with its status as Success / Failed. For successful transaction you will have option to view Receipt/certificate.
